package com.supermap.desktop.enums;

public enum TextStyleType {
	ALIGNMENT, // 文本对齐方式
	BACKCOLOR, // 文本背景色
	FORECOLOR, // 文本前景色
	BACKOPAQUE, // 文本背景透明
	BOLD, // 文本是否为粗体
	BORDERSPACINGWIDTH, // 文字背景矩形框边缘与文字边缘的间隔
	FONTSIZE,// 字高
	FONTHEIGHT, // 字高
	FONTNAME, // 字体名称
	FONTSCALE, // 字体缩放比例
	FONTWIDTH, // 字宽
	ITALIC, // 文本是否为斜体
	ITALICANGLE, // 文本倾斜角度
	OPAQUERATE, // 文字的不透明度
	OUTLINE, // 以轮廓的方式显示文本
	OUTLINEWIDTH, // 轮廓宽度
	ROTATION, // 文本旋转角度
	FIXEDSIZE, // 固定大小
	SHADOW, // 是否添加阴影
	STRIKOUT, // 是否添加删除线
	STRINGALIGNMENT, // 文本排版方式
	UNDERLINE, // 下划线
	WEIGHT;// 字体的磅数，表示粗体的具体数值
}
